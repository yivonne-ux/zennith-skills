def full_pipeline(img, preset="default", logo_path=None, logo_in_design=False,
                  target_w=1080, target_h=1350, top_bias=0.3):
    """
    Run the complete post-processing pipeline in correct order.
    Order: fit -> filter -> logo -> grain (NEVER reorder)
    """
    p = FILTER_PRESETS.get(preset, FILTER_PRESETS["default"])

    # 1. Fit
    img = fit_target(img, target_w, target_h, top_bias)

    # 2. Brand filter
