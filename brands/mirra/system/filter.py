def _hex_to_rgb(hex_str):
    """Convert hex color to RGB tuple."""
    h = hex_str.lstrip('#')
    return tuple(int(h[i:i+2], 16) for i in (0, 2, 4))


def fit_target(img, target_w=1080, target_h=1350, top_bias=0.3):
    """
    Crop and resize image to target dimensions.
    top_bias: 0.0 = crop from top, 0.5 = center, 1.0 = crop from bottom.
    For UI screenshots, use top_bias=0.15 to preserve nav bars.
    """
    src_w, src_h = img.size
    target_ar = target_w / target_h
    src_ar = src_w / src_h

    if abs(src_ar - target_ar) < 0.01:
        return img.resize((target_w, target_h), Image.LANCZOS)

    if src_ar > target_ar:
        # Source is wider — crop sides
        new_w = int(src_h * target_ar)
        left = (src_w - new_w) // 2
        img = img.crop((left, 0, left + new_w, src_h))
    else:
        # Source is taller — crop top/bottom with bias
        new_h = int(src_w / target_ar)
        top = int((src_h - new_h) * top_bias)
        img = img.crop((0, top, src_w, top + new_h))

    return img.resize((target_w, target_h), Image.LANCZOS)


def brand_filter(img, overlay_rgb=None, overlay_alpha=42, desat=0.80, brightness=1.04, contrast=0.95):
    """
    Apply brand filter stack.
    overlay_rgb: tuple (R, G, B) — brand overlay color. Uses palette default if None.
    overlay_alpha: 0-255. Higher = stronger overlay.
    desat: 0.0 = full gray, 1.0 = no change.
    """
    img = img.convert("RGB")
    arr = np.array(img, dtype=np.float64) / 255.0

    # 1. Brand overlay tint
    if overlay_rgb is None:
        # Use first palette color as default overlay
        overlay_rgb = OVERLAY_BG if 'OVERLAY_BG' in dir() else PRIMARY_BLUSH if 'PRIMARY_BLUSH' in dir() else (245, 220, 210)
    ov = np.array(overlay_rgb, dtype=np.float64) / 255.0
    alpha = overlay_alpha / 255.0
    arr = arr * (1.0 - alpha) + ov * alpha

    # 2. Desaturation
    gray = np.mean(arr, axis=2, keepdims=True)
    arr = gray * (1.0 - desat) + arr * desat

    # 3. Brightness + contrast
    arr = np.clip(arr * brightness, 0, 1)
    mean = np.mean(arr)
    arr = mean + (arr - mean) * contrast
    arr = np.clip(arr, 0, 1)

    return Image.fromarray((arr * 255).astype(np.uint8))


def stamp_logo(img, logo_path=None, position="bottom-right", scale=0.08, margin=30):
    """
    Stamp brand logo on image.
    Only call if logo_in_design=False.
    """
    if logo_path is None or not os.path.exists(logo_path):
        return img

    logo = Image.open(logo_path).convert("RGBA")
    logo_w = int(img.width * scale)
    logo_h = int(logo.height * (logo_w / logo.width))
    logo = logo.resize((logo_w, logo_h), Image.LANCZOS)

    pos_map = {
        "bottom-right": (img.width - logo_w - margin, img.height - logo_h - margin),
        "bottom-left": (margin, img.height - logo_h - margin),
        "bottom-center": ((img.width - logo_w) // 2, img.height - logo_h - margin),
        "top-right": (img.width - logo_w - margin, margin),
        "top-left": (margin, margin),
    }
    pos = pos_map.get(position, pos_map["bottom-right"])

    canvas = img.convert("RGBA")
    canvas.paste(logo, pos, logo)
    return canvas.convert("RGB")


def add_grain(img, strength=0.014):
    """
    Add film grain. ALWAYS apply LAST in the pipeline.
    strength: 0.014 for UI designs, 0.022 for flat typography backgrounds.
    """
    arr = np.array(img, dtype=np.float64)
    noise = np.random.normal(0, strength * 255, arr.shape)
    arr = np.clip(arr + noise, 0, 255)
    return Image.fromarray(arr.astype(np.uint8))


def _treat_product_photo(product_path, zone_ar=(3, 4), warm_tint=0.08, desat=0.10):
    """
    Prepare a product photo for Pipeline B/C insertion.
    Crops to zone aspect ratio, applies warm tint + slight desaturation.
    Zone ARs: (3,4) portrait, (16,9) landscape banner, (4,3) landscape, (1,1) square.
    """
    food = Image.open(product_path).convert("RGB")

    # Center crop to target AR
    src_w, src_h = food.size
    target_ar = zone_ar[0] / zone_ar[1]
    src_ar = src_w / src_h
    if src_ar > target_ar:
        new_w = int(src_h * target_ar)
        left = (src_w - new_w) // 2
        food = food.crop((left, 0, left + new_w, src_h))
    else:
        new_h = int(src_w / target_ar)
        top = (src_h - new_h) // 2
        food = food.crop((0, top, src_w, top + new_h))

    # Warm tint
    arr = np.array(food, dtype=np.float64) / 255.0
    warm_overlay = np.ones_like(arr)
    warm_overlay[:, :, 0] = 1.0    # R
    warm_overlay[:, :, 1] = 0.85   # G (amber)
    warm_overlay[:, :, 2] = 0.65   # B
    arr = arr + warm_tint * (warm_overlay - arr)

    # Partial desaturation
    gray = np.mean(arr, axis=2, keepdims=True)
    arr = gray * desat + arr * (1.0 - desat)
    arr = np.clip(arr, 0, 1)

    return Image.fromarray((arr * 255).astype(np.uint8))


